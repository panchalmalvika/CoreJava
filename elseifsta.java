package Test.first;

public class elseifsta {
	public static void main(String[] args) {
		int total=88;
		if(total>=90)
		{
			System.out.println("First rank:");
		}
		else if(total>=80)
		{
			System.out.println("Second rank:");
		}
		else if(total>=70)
		{
			System.out.println("Third rank:");
		}
		else if(total>=55)
		{
			System.out.println("pass rank:");
		}
		else
		{
			System.out.println("fail");
		}
	}
}
